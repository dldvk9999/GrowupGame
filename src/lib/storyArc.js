/**
 * 스테이지 이동마다 낮은 확률(기본 12%)로 인터루드 하나를 반환, 아니면 null.
 * 챕터 최초 진입 스토리(getChapterStory)와는 별개 경로 - 이미 지나간 챕터를
 * 반복 도전할 때도 뜰 수 있어서, 몇 번을 다시 가더라도 매번 같은 장면이 아니라
 * 풀에서 무작위로 골라 보여줌(연속 등장 방지를 위해 직전과 다른 걸 고름).
 */
export function maybeGetInterlude(chance = 0.12, excludeTitle = null) {
  if (Math.random() >= chance) return null;
  const pool = excludeTitle ? INTERLUDES.filter((i) => i.title !== excludeTitle) : INTERLUDES;
  return pool[Math.floor(Math.random() * pool.length)];
}
